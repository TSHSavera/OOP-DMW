    //On load, change the text of the labels to the data from the database
    public void loadData() {
        //Query User Profile
        SQLResultParser sqlResultParser = new SQLResultParser() {
            @Override
            public ArrayList<Map> sqlQuery() {
                //Create a new SQLHandler object
                SQLHandler sqlHandler = new SQLHandler();
                //Create a new query
                String query = "SELECT * FROM user WHERE UserID = " + Auth.userId;
                //Execute the query
                sqlHandler.createQuery(query).executeQuery();
                //Return the results
                return sqlHandler.getResults();
            }
        };

        //Set the results to a jLabels
        jLabel3.setText(sqlResultParser.parseResults().getValueByKey("FirstName").toString());
        jLabel4.setText(sqlResultParser.parseResults().getValueByKey("MiddleName").toString());
        jLabel5.setText(sqlResultParser.parseResults().getValueByKey("LastName").toString());
        jLabel6.setText(sqlResultParser.parseResults().getValueByKey("Suffix").toString());

        jLabel9.setText(sqlResultParser.parseResults().getValueByKey("EmailAddress").toString());
        jLabel14.setText(sqlResultParser.parseResults().getValueByKey("Birthdate").toString());
        jLabel20.setText(sqlResultParser.parseResults().getValueByKey("Gender").toString());

        //Query profile data
        sqlResultParser = new SQLResultParser() {
            @Override
            public ArrayList<Map> sqlQuery() {
                //Create a new SQLHandler object
                SQLHandler sqlHandler = new SQLHandler();
                //Create a new query
                String query = "SELECT * FROM profile WHERE UserID = " + Auth.userId;
                //Execute the query
                sqlHandler.createQuery(query).executeQuery();
                //Return the results
                return sqlHandler.getResults();
            }
        };

        if (sqlResultParser.getResultsSize() == 0) {
            //Clear the text in JLabels
            jLabel11.setText("");
            jLabel16.setText("");
            jLabel18.setText("");
            jLabel22.setText("");
            jLabel24.setText("");
            jLabel27.setText("");
            jLabel29.setText("");
            jLabel38.setText("");
            jLabel40.setText("");
            jLabel31.setText("");
            jLabel33.setText("");
            jLabel35.setText("");

        } else {
            //Set the results to a jLabels
            jLabel11.setText(sqlResultParser.parseResults().getValueByKey("ContactNo").toString());
            jLabel16.setText(sqlResultParser.parseResults().getValueByKey("Height").toString());
            jLabel18.setText(sqlResultParser.parseResults().getValueByKey("Weight").toString());
            jLabel22.setText(sqlResultParser.parseResults().getValueByKey("CivilStatus").toString());
            jLabel24.setText(sqlResultParser.parseResults().getValueByKey("Religion").toString());

            //Set Address
            jLabel27.setText(sqlResultParser.parseResults().getValueByKey("House").toString());
            jLabel29.setText(sqlResultParser.parseResults().getValueByKey("StreetNo").toString());

            //Set Place of Birth
            jLabel38.setText(sqlResultParser.parseResults().getValueByKey("Birthplace_Mun").toString());
            jLabel40.setText(sqlResultParser.parseResults().getValueByKey("Birthplace_Pro").toString());

            //Convert Barangay ID to Name
            SQLResultParser finalSqlResultParser = sqlResultParser;
            sqlResultParser = new SQLResultParser() {
                @Override
                public ArrayList<Map> sqlQuery() {
                    //Create a new SQLHandler object
                    SQLHandler sqlHandler = new SQLHandler();
                    //Create a new query
                    String query = "SELECT * FROM barangay WHERE BarangayID = '" + finalSqlResultParser.parseResults().getValueByKey("BarangayID").toString() + "'";
                    //Execute the query
                    sqlHandler.createQuery(query).executeQuery();
                    //Return the results
                    return sqlHandler.getResults();
                }
            };
            jLabel31.setText(sqlResultParser.parseResults().getValueByKey("BarangayName").toString());

            //Convert Municipality ID to Name
            sqlResultParser = new SQLResultParser() {
                @Override
                public ArrayList<Map> sqlQuery() {
                    //Create a new SQLHandler object
                    SQLHandler sqlHandler = new SQLHandler();
                    //Create a new query
                    String query = "SELECT * FROM municipalities WHERE MunicipalityID = '" + finalSqlResultParser.parseResults().getValueByKey("MunicipalityID").toString() + "'";
                    //Execute the query
                    sqlHandler.createQuery(query).executeQuery();
                    //Return the results
                    return sqlHandler.getResults();
                }
            };
            jLabel33.setText(sqlResultParser.parseResults().getValueByKey("MunicipalityName").toString());

            //Convert Province ID to Name
            sqlResultParser = new SQLResultParser() {
                @Override
                public ArrayList<Map> sqlQuery() {
                    //Create a new SQLHandler object
                    SQLHandler sqlHandler = new SQLHandler();
                    //Create a new query
                    String query = "SELECT * FROM provinces WHERE ProvinceID = '" + finalSqlResultParser.parseResults().getValueByKey("ProvinceID").toString() + "'";
                    //Execute the query
                    sqlHandler.createQuery(query).executeQuery();
                    //Return the results
                    return sqlHandler.getResults();
                }
            };
            jLabel35.setText(sqlResultParser.parseResults().getValueByKey("ProvinceName").toString());
        }

    }

